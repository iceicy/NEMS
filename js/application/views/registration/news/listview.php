                        <h4> <a href="">โครงการกีฬาสานสัมพันธ์ไอทีสามพระจอมเกล้าฯ ครั้งที่ 14</a>
                        </h4>
                        <p>สโมสรนักศึกษาคณะเทคโนโลยีสารสนเทศ มหาวิทยาลัยเทคโนโลยีพระจอมเกล้าธนบุรี ร่วมกับ สโมสรนักศึกษาคณะเทคโนโลยีสารสนเทศ สถาบันเทคโนโลยีพระจอมเกล้าเจ้าคุณทหารลาดกระบัง และนักศึกษาภาควิชาเทคโนโลยีสารสนเทศ คณะเทคโนโลยีอุตสาหกรรมและการจัดการ มหาวิทยาลัยเทคโนโลยีพระจอมเกล้าพระนครเหนือ วิทยาเขตปราจีนบุรี จัดโครงการกีฬาสานสัมพันธ์ไอทีสามพระจอมเกล้าฯ ครั้งที่ 14  ตอน Once Upon a Time ณ กาลครั้งหนึ่ง  ณ คณะเทคโนโลยีอุตสาหกรรมและการจัดการ มหาวิทยาลัยเทคโนโลยีพระจอมเกล้าพระนครเหนือ วิทยาเขตปราจีนบุรี เมื่อวันที่ 4 กุมภาพันธ์ที่ผ่านมา</p>
